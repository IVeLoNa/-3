//#2Пусть дано целое число a. Вывести вначале значения его битов с четными индексами, а затем – с нечетными.
#include <stdio.h> 

// Функция для вывода битов целого числа
void printBits(int a) {
    // Вывод битов с четными индексами
    printf("Биты с четными индексами: "); // Сообщение перед выводом битов
    // Цикл для обхода всех битов числа a
    for (int i = 0; i < sizeof(a) * 8; i += 2) {
        // Сдвигаем число a вправо на i позиций и применяем побитовую операцию AND с 1
        // Это позволяет получить значение бита на позиции i
        printf("%d ", (a >> i) & 1);
    }

    printf("n"); // Переход на новую строку для лучшей читаемости

    // Вывод битов с нечетными индексами
    printf("Биты с нечетными индексами: "); // Сообщение перед выводом битов
    // Цикл для обхода всех битов числа a
    for (int i = 1; i < sizeof(a) * 8; i += 2) {
        // Аналогично предыдущему циклу, но начинаем с 1 и увеличиваем на 2
        printf("%d ", (a >> i) & 1);
    }

    printf("n"); // Переход на новую строку после вывода всех битов
}

int main() {
    int a; // Объявляем переменную для хранения введенного числа

    // Запрашиваем у пользователя ввод целого числа
    printf("Введите целое число: ");
    scanf("%d", &a); // Считываем число с консоли и сохраняем его в переменной a

    // Вызываем функцию для вывода битов числа a
    printBits(a);

    return 0; // Завершаем программу и возвращаем 0, что означает успешное завершение
}

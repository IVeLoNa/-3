//#3.Пусть дано целое число a. Проверить, чередуются ли в нем единичные и нулевые биты.
#include <stdio.h>

// Функция для проверки чередования битов
int areBitsAlternating(int a) {
    // Сдвигаем число вправо на 1 и сравниваем с оригинальным
    // Если два соседних бита одинаковы, то результат будет не равен 0
    return (a & 1) ^ ((a >> 1) & 1); // Проверяем первые два бита
}

int main() {
    int a; // Объявляем переменную для хранения введенного числа

    // Запрашиваем у пользователя ввод целого числа
    printf("Введите целое число: ");
    scanf("%d", &a); // Считываем число с консоли и сохраняем его в переменной a

    // Проверяем чередование битов
    int alternating = 1; // Предполагаем, что биты чередуются
    for (int i = 0; i < sizeof(a) * 8 - 1; i++) {
        // Если текущий бит равен следующему, значит они не чередуются
        if (((a >> i) & 1) == ((a >> (i + 1)) & 1)) {
            alternating = 0; // Биты не чередуются
            break; // Выходим из цикла
        }
    }

    // Выводим результат
    if (alternating) {
        printf("Биты числа %d чередуются", a);
    } else {
        printf("Биты числа %d не чередуются", a);
    }

    return 0; // Завершаем программу
}
